package EjercicioParcial;

/**
 *
 * @author carlo
 */
public class InterfazRed {
    
    private int A, B, C, D;
    private String IP;
    
    
    public InterfazRed(int A, int B, int C, int D){
        this.A = A;
        this.B = B;
        this.C = C;
        this.D = D;
    }

    /**
     * @return the A
     */
    public int getA() {
        return A;
    }

    /**
     * @param A the A to set
     */
    public void setA(int A) {
        this.A = A;
    }

    /**
     * @return the B
     */
    public int getB() {
        return B;
    }

    /**
     * @param B the B to set
     */
    public void setB(int B) {
        this.B = B;
    }

    /**
     * @return the C
     */
    public int getC() {
        return C;
    }

    /**
     * @param C the C to set
     */
    public void setC(int C) {
        this.C = C;
    }

    /**
     * @return the D
     */
    public int getD() {
        return D;
    }

    /**
     * @param D the D to set
     */
    public void setD(int D) {
        this.D = D;
    }

    /**
     * @return the IP
     */
    public String getIP() {
        return IP;
    }

    /**
     * @param IP the IP to set
     */
    public void setIP(String IP) {
        this.IP = IP;
    }

    

    
    
}
